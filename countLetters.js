const countLetters = function(stringOfLetters) {
  stringOfLetters = stringOfLetters.replace(/ /g, "");
  const results = {};
  for (const letter of stringOfLetters) {
    if (results[letter]) {
      results[letter] += 1;
    } else {
      results[letter] = 1;
    }
  }
  return results;
};
